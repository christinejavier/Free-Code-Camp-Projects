function getQuote() {
  $("#quoteTitle").hide();
  $(".wherePixWillBe").hide();
  $("#getMessage").text("NEXT QUOTE");
  var imgCount = 30;
  var randomCount = Math.round(Math.random() * (imgCount - 1)) + 1;
  document.body.style.backgroundImage = 'url(' + images[randomCount] +
    ')';
  document.body.style.backgroundSize = "cover";
  document.body.style.backgroundAttachment = "fixed";
  document.body.style.backgroundPosition = "center center";

  $.ajax({
    method: "GET",
    url: "http://api.forismatic.com/api/1.0/?method=getQuote&lang=en&format=jsonp&jsonp=?",
    dataType: "jsonp",
    error: function() {
      console.log("this doesn't work");
    },
    success: function(response) {
      $("#quote").text('"' + response.quoteText + '"');
      $("#quoteSayer").text('-' + response.quoteAuthor);
    }
  });
}

$(document).ready(function() {
  $("#getMessage").click(getQuote);
});
